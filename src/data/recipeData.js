// Helper function to get recipes by category
export const getRecipesByCategory = (category) => {
  if (category === 'all') return allRecipes
  return allRecipes.filter(recipe => 
    recipe.category.toLowerCase() === category.toLowerCase()
  )
}

// Helper function to get recipes by tags
export const getRecipesByTags = (tag) => {
  return allRecipes.filter(recipe => 
    recipe.tags.toLowerCase().includes(tag.toLowerCase())
  )
}

// Helper function to search recipes
export const searchRecipes = (query) => {
  const lowercaseQuery = query.toLowerCase()
  return allRecipes.filter(recipe => 
    recipe.name.toLowerCase().includes(lowercaseQuery) ||
    recipe.tags.toLowerCase().includes(lowercaseQuery) ||
    recipe.category.toLowerCase().includes(lowercaseQuery)
  )
}